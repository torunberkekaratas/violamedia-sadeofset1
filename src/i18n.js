const TITLES = {
  en: 'Sade Ofset — Printing & Packaging',
  tr: 'Sade Ofset — Baskı & Ambalaj',
};

const els = Array.from(document.querySelectorAll('[data-i18n]'));
// Snapshot the original English markup once.
const EN = new Map();
els.forEach((el) => EN.set(el, el.innerHTML));

function applyLang(lang) {
  const useTr = lang === 'tr';
  els.forEach((el) => {
    const key = el.dataset.i18n;
    const next = useTr && TR[key] != null ? TR[key] : EN.get(el);
    if (el.innerHTML !== next) el.innerHTML = next;
  });

  document.documentElement.lang = lang;
  document.title = TITLES[lang] || TITLES.en;

  document.querySelectorAll('.lang__btn').forEach((btn) => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle('is-active', active);
    btn.setAttribute('aria-pressed', active ? 'true' : 'false');
  });

  try {
    localStorage.setItem('sade-lang', lang);
  } catch (e) {
    /* storage unavailable — ignore */
  }
}

// Wire up the flag buttons.
document.querySelectorAll('.lang__btn').forEach((btn) => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

// Initial language: stored choice → browser preference → English.
let initial = 'en';
try {
  const saved = localStorage.getItem('sade-lang');
  if (saved === 'tr' || saved === 'en') initial = saved;
  else if (navigator.language && navigator.language.toLowerCase().startsWith('tr'))
    initial = 'tr';
} catch (e) {
  /* ignore */
}

applyLang(initial);
